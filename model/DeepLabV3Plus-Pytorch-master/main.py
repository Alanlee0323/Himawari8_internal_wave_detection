class BinarySegmentation(data.Dataset):
    """二元分割數據集"""
    
    def __init__(self, root, split='train', transform=None):
        self.root = root
        self.split = split
        self.transform = transform
        self.images = []
        self.masks = []
        
        # 設定圖片和mask的目錄
        img_dir = os.path.join(root, f'{split}_images')  
        mask_dir = os.path.join(root, f'{split}_masks')
        
        # 獲取所有圖片文件
        for filename in os.listdir(img_dir):
            if filename.endswith(('.png', '.jpg', '.jpeg', '.tif')):
                img_path = os.path.join(img_dir, filename)
                mask_name = os.path.splitext(filename)[0] + '.png'
                mask_path = os.path.join(mask_dir, mask_name)
                
                if os.path.exists(mask_path):
                    self.images.append(img_path)
                    self.masks.append(mask_path)

         # 添加數據檢查
        if len(self.images) == 0:
            raise RuntimeError(f'在{root}/{split}_images中找不到圖片文件!')
        
        # 打印數據集信息
        print(f'[Dataset Info] {split}集: 找到{len(self.images)}張圖片')
        print(f'[Dataset Info] 圖片目錄: {img_dir}')
        print(f'[Dataset Info] 標註目錄: {mask_dir}')

    def __len__(self):
        return len(self.images)

    def __getitem__(self, index):
        img_path = self.images[index]
        mask_path = self.masks[index]
        
        try:
            image = Image.open(img_path).convert('RGB')
            target = Image.open(mask_path).convert('L')
            
            # 檢查圖片尺寸
            if image.size != target.size:
                raise ValueError(f'圖片和標註尺寸不匹配! 圖片:{image.size}, 標註:{target.size}')
            
            # 檢查標註值是否二值化
            target_array = np.array(target)
            unique_values = np.unique(target_array)
            if not all(v in [0, 255] for v in unique_values):
                print(f'Warning: 標註{mask_path}不是二值圖像,包含值:{unique_values}')
        
             # 讀取圖片和mask
            image = Image.open(img_path).convert('RGB')
            target = Image.open(mask_path).convert('L')
        
            # 二值化mask
            target = np.array(target)
            target = (target > 128).astype(np.uint8)
            target = Image.fromarray(target)
        
            if self.transform:
                image, target = self.transform(image, target)

        except Exception as e:
            print(f'Error processing {img_path} or {mask_path}: {str(e)}')
            raise e
        
        return image, target
    
    @staticmethod
    def decode_target(mask):
        """將mask轉換為RGB圖像用於可視化"""
        mask = np.array(mask)
        rgb = np.zeros((mask.shape[0], mask.shape[1], 3), dtype=np.uint8)
        rgb[mask == 1] = [255, 255, 255]  # 前景為白色
        return rgb

